<b>What is Alpha Phi Omega?</b><br/>

<p>Community service. Making new friends. Traveling all over the nation. Learning to put aside your fears and lead others. These are all Alpha Phi Omega.</p>

<p>Alpha Phi Omega is an international co-ed service fraternity. We are an organization dedicated to serving mankind and developing ourselves to become leaders and friends to humanity. To those ends, we create our own democracy. We train our people to be excellent leaders and creative team members. We open our hearts and minds to people and show that the newest generation of adults <b>do</b> care.</p>

<p><b>Our Goal</b></p>

<p><em>The purpose of this Fraternity shall be to assemble college students in a National Service Fraternity in the fellowship of principles derived from the Scout Oath and Law of the Boy Scouts of America; to develop Leadership, to promote Friendship, and provide Service to humanity; and to further the freedom that is our national, educational, and intellectual heritage.</em></p>

<p><b>National History</b></p>

<p>The history of Alpha Phi Omega then is a story of Leadership, Friendship and Service. Since the founding at Lafayette College in 1925, more than 255,000 students have participated in this nationwide Brotherhood. From a single chapter in 1925, this National Service Fraternity has grown to 717 chapters.</p>

<p>Following the chartering of Beta Chapter at the University of Pittsburgh, three more chapters were chartered in 1927--Gamma at Cornell University on February 17, Delta at Auburn University on November 8, and Epsilon at Northeast Missouri State University on December 13. And with the chartering of the sixth chapter--Zeta at Stanford University--on May 19, 1928, Alpha Phi Omega in its first four years had spread from the East to the South to the Midwest and all the way to the Pacific Coast. The Fraternity has continued to grow, having chartered more chapters than any other collegiate organization.</p>

<p>In the very early years, decisions of the National Fraternity were made by mail. The first actual assembly of delegates in convention was in St. Louis, Missouri, March 1 and 2, 1931. By that date the Fraternity had grown to 18 chapters. Seven of the chapters were represented at the convention by a total of 23 students and advisors.</p>

<p>Thirty-eight biennial National Conventions have been held (two were skipped in 1942 and 1944 because of World War II). A special Constitutional Convention was held in 1967.</p>

<p>At the 1931 Convention, the presiding officer was Frank R. Horton. He had served as Supreme Grand Master (National President) from the beginning of the organization until that time. The convention elected Dr. H. Roe Bartle to succeed our Founder as Supreme Grand Master.</p>

<p>For years, Alpha Phi Omega state conventions were held. Today chapters are grouped in "sections" and "regions." There are 54 sections in 11 regions across the country. Sectional and Regional Conferences are a very important part in our Fraternity operation. They foster good fellowship and helpful exchange of ideas for the advancement of our service program.</p>

<p>At the 1976 National Convention in Atlanta, Georgia, the delegates voted to open the ranks of full membership to women, thereby making the Alpha Phi Omega experience available to all students.</p>

<p><b>Iota Phi History</b></p>

<p>On May 27, 1951, the national office installed thirty-four students, three scouting advisors, six faculty, and four honorary members as the Iota Phi chapter of Alpha Phi Omega. Gamma Gamma chapter (UC Berkeley) and Chi chapter (UCLA) brought spirit and ideas of Alpha Phi Omega to Davis. Iota Phi flourished in its early days under its first president, William Aylett Sparkes. Our founding brothers include Harry A Laidlaw, Jr. (UCD Faculty), Claude Burton Hutchinson (UCD Dean, 66 years of age), and Knowles A. Ryerson (UCD Professor, 59 years of age).  Incidentally, these are the two same people that Hutchinson and Ryerson Halls are named after.  By the records, 26 of the founding brothers had graduated or would graduate by 1952. Apparently, the chapter graduated <em>en masse</em>, leading to the de-activation of the Iota Phi chapter in the mid 1950's.</p>

<p>The spirit of Iota Phi rekindled in 1961 under the direction of Professor Harlan Pratt and his "Iota Phi Club." The chapter eventually reactivated with nine initiated, including their elected president, Oliver Michaelis, on September 20, 1962. These inspired and dedicated men, tried desperately to build a strong chapter from which the spirit of humanitarianism would grow. The next pledge class brought forth 5 more brothers. The third pledge class of Iota Phi included the notable Emil M.  Mrak (Mrak Hall is named after him) and a total of 12 more brothers, doubling the chapter size.  The chapter went through its ups and downs until around 1979, when David A. Emery, Deborah Sloane, and Elizabeth Tom pledged. These three were critical in establishing 'Families,' a tradition which we continue to this day.</p>

<p>The reborn Iota Phi chapter has progressively grown in strength and number since 1962 and going co-ed in 1976, and it has developed a broad service program encompassing service to the nation, community, and campus.  Our service projects include Santa For Kids (a program created by our chapter for the HeadStart program), planting in the Arboretum, Special Olympics, trick-or-treating for UNICEF, various projects associated with Picnic Day on campus, Examiner Bay to Breakers, among other noteworthy projects. </p>
